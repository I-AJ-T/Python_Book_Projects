### Variables and naming them 

print("Hello Python World!")
# 'print' Command displays text to the screen 
message = "Hello Python Crash Course World"
# Here 'message' is a variable and the "Hello Python Crash Course World" is the data held in the 'message' variable 
# Variables cannot start with a number and spaces are not allowed in them  
print(message)
# 'message' variable in the 'print' command is used to display the data held in the 'message' variable
